x3,y3 = p3.range()
p4 = prj.Projectile()
p4.init(30, 0, 0, 45, 1.22, 1, 1, 0.001, 0.01)
x4,y4 = p4.rangeRungeKutta()
plt.plot(x3,y3,c="r")
plt.plot(x4,y4,c="b")
plt.xlabel("x [m]")
plt.ylabel("y [m]")
plt.title("Usporedba putanje kosog hica\n Eulera i Runge-Kutta")
plt.legend(["Euler", "Runge-Kutta"])
plt.show()
